              <h3>WHAT IS REAL ESTATE DEPRECIATION?</h3>
              <p>A depreciation is the accounting name for a decrease in the value of assets. This reduction in value can be claimed as an operating expense for tax purposes and is usually called AfA (deduction for wear and tear).</p>
              <p>In the case of <a href="#" class="a_color text-decoration-underline">real estate</a> , the rule applies that acquisition costs for the land are not deductible, since their value is not reduced by wear and tear. Acquisition costs for buildings, on the other hand, can already be written off.</p>
              
              <h6 class="mb-4">How is depreciation calculated in Austria?</h6>
              
              <p>In general, the acquisition costs of the property are used as the basis for assessment. This includes the net purchase price plus ancillary acquisition costs (for broker, <a href="#" class="a_color text-decoration-underline">notary</a> , lawyer, <a href="#" class="a_color text-decoration-underline">escrow account</a> , fees, etc.) as well as subsequent acquisition costs (for development, <a href="#" class="a_color text-decoration-underline">maintenance</a> in the near future).</p>
              <p>Since 2016, it has been assumed that 40% of the acquisition costs for land are incurred (also for <a href="#" class="a_color text-decoration-underline">condominiums</a> !), which cannot be written off. On the other hand, 60% of the costs are for the building and can be written off. (However, this ratio can be divided differently by an expert opinion, and the basic share decreases under certain circumstances due to the Basic Share Ordinance 2016 - which is advantageous for deductibility, as a higher share remains for the building).</p>
              
              <h6 class="mb-4">How much is depreciation in Austria?</h6>
              
              <p>A fixed, defined depreciation rate can be depreciated annually from the acquisition costs calculated above. In the operational area, this is 2.5%, for rental and leasing in the non-operational area only 1.5% (2% for buildings erected before 1915).</p>
