              <h3>DEFINITION: THE LAND REGISTER IN AUSTRIAN REAL ESTATE LAW</h3>
              <p class="mb-0">The land register is a public directory in which, for example, ownership of a <a href="#" class="text-decoration-underline a_color">property</a> (including real estate), but also <a href="#" class="text-decoration-underline a_color">residential property , lien</a> or building rights are recorded.</p>
              <p>It is managed by the district courts in Austria and is publicly accessible.</p>
              <p>Anyone who buys a plot of <a href="#" class="text-decoration-underline a_color">land , a house</a> or <a href="#" class="text-decoration-underline a_color">an apartment must find out from the land register who the so-called "book owner" is and whether any encumbrances have been entered.</a></p>
              <p><a href="terms.php" class="text-decoration-underline a_color">Legal dictionary: Overview of all legal terms of housing law and real estate law</a></p>
              
              <h6 class="mb-4">What is in the ledger?</h6>
              
              <p>It consists of the main ledger, the list of deleted entries, the auxiliary lists, the collection of documents and the land register folder.</p>
              
              <h6 class="mb-4">Where can I see the land register?</h6>
              
              <p>At the competent court. As described above, the land register in Austria is kept by the district courts (but not by the district court for commercial matters in Vienna).</p>
              <p>Inspection is also possible with any <a href="#" class="text-decoration-underline a_color">notary</a> , who can also produce and certify extracts from the land register.</p>
              <p>Data from the real estate database can also be queried quite easily online; authorized sites are available for this purpose.</p>
              <p>However, an excerpt may be difficult for laypersons to understand. Here a lawyer or <a href="#" class="text-decoration-underline a_color">notary</a> can provide a safe remedy. The correct understanding of the extract from the land register and the recording of all relevant data is your own responsibility.</p>
              
              <h6 class="mb-4">What are the costs of a land registry entry?</h6>
              
              <p>If you buy a property and want to have your new property entered in the land register, this is called a registration. In any case, it is advisable to have a lawyer or notary assist you with this.</p>
              <p>Normally, a contract of sale cannot be entered into Grund immediately. First, registered <a href="#" class="text-decoration-underline a_color">liens and mortgages</a> must be cleared. <a href="#" class="text-decoration-underline a_color">In addition, the real estate transfer tax</a> must first be paid. Certain permits may also be required.</p>
              <p class="mb-0">If it is finally entered in the land register, the following costs are incurred:</p>
              <p class="mb-0">- Entry fee for the land register application: approx. 44 - 62 euros</p>
              <p class="mb-0">- Entry fee: 1.1% of the purchase price (is subsequently prescribed by the competent court)</p>
              <p>- Entry of a lien (because the purchase financed with a loan): 1.2% of the deposit amount</p>
              
              <h6 class="mb-4">How much does an excerpt from the land register cost?</h6>
              
              <p class="mb-0">The fees for land register excerpts from the main register or from the auxiliary registers amount to 13.70 euros at the district court.</p>
              <p class="mb-0">Different costs are incurred for the online query of the "new property database", depending on the requirement.</p>
              <p class="mb-0">Here are a few examples:</p>
              <p class="mb-0">Current extract from the land register: EUR 3.53 for a full query of a deposit number</p>
              <p class="mb-0">Current partial extract: EUR 1.88</p>
              <p class="mb-0">Query of the collection of documents: EUR 1.10 per document</p>
              <p>Query of the list of persons: EUR 1.76 per person</p>
