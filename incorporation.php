              <p>Incorporation (also intabulation) is a specific form of entry in the land register. The entry in the <a href="land_register.php" class="a_color text-decoration-underline">land register</a> causes either an unconditional acquisition of rights or, in the event of deletion, a loss of rights and is a prerequisite for the acquisition of real rights to real estate.</p>
              <p><a href="terms.php" class="a_color text-decoration-underline">Legal dictionary: Overview of all legal terms of housing law and real estate law</a></p>
              
              <h6 class="mb-4">How is an incorporation formally carried out?</h6>
              
              <p>From a formal point of view, the incorporation is carried out by means of an application to the competent land register court. The contracting party is usually commissioned and authorized by the parties to carry out the purchase contract in the land register.</p>
              
              <h6 class="mb-4">Is incorporation a prerequisite for buying a property?</h6>
              
              <p>Incorporation is not a prerequisite for the purchase of a property. It is also possible to acquire property off the books. In this case, however, you also lose the protective effects associated with incorporation (trust in the status of the land register!).</p>
              
              <h6 class="mb-4">As a buyer, what do I have to pay attention to when registering?</h6>
              
              <p>As applicants, buyers must ensure that the necessary documents are available in the form prescribed by law. This is important for the buyer, since a defect in form can lead to an order for improvement from the court.</p>
              Within this court-mandated period of improvement, the applicant must make the improvement or state that a court decision is sought. Otherwise the application is deemed withdrawn.
              
              <h6 class="mb-4">How is an incorporation structured in terms of content?</h6>
              
              <p>In terms of content, the land register application consists of the data relevant to the purchase contract from the contracting parties and any other parties involved (e.g. banks) and a specific request (e.g. deletion, transfer of ownership).</p>
              The documents required for the land register application (e.g. purchase contract) must be enclosed.
              
              <h6 class="mb-4">Is there a template for land register applications or does this come from a lawyer?</h6>
              
              <p>Land register applications can also be made orally for the record at the court. The prerequisite, however, is that it is a “simple” case. A case is easy if the recording by the court involves a reasonable amount of work (e.g. request for deletion).</p>
              <p>As a rule, the land register implementation and processing takes place via the so-called electronic legal transactions (ERV) of the lawyers/ <a href="#" class="a_color text-decoration-underline">notaries</a> . The advantage is that the processing can be carried out faster due to the possibility of digital submission of documents.</p>
