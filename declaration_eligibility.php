              <p>With the declaration of attachment in a purchase contract, the person entitled to the property (seller's side) makes a declaration that he consents to the <a href="#" class="a_color text-decoration-underline">incorporation</a> of another person (buyer's side) and thus agrees to the entry of the buyer in the <a href="land_register.php" class="a_color text-decoration-underline">land register .</a> The declaration of eligibility is a prerequisite for incorporation in the land register.</p>
