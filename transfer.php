              <h3>DEFINITION OF TRANSFER FEE FOR REAL ESTATE IN AUSTRIA</h3>
              <p>The transfer is a one-off payment that the tenant has to make to the landlord or the previous tenant in connection with the conclusion of the contract.</p>
              
              <h6 class="mb-4">When may no fee be demanded?</h6>
              
              <p>In the full application area of the MRG (Tenancy Law) there is a transfer ban. Read all about the <a href="#" class="a_color text-decoration-underline">applicability of the Tenancy Law</a> under this link.</p>
              <p>So-called “investment redemptions” are excluded. If a landlord pays such a fee to the tenant who is moving out, he can also demand an investment fee from the new tenant (details on this can be found below) - but this has no place in the rent! In such a case, a look at all receipts is always a good idea.</p>
              <p><a href="#" class="a_color text-decoration-underline">>> Discovered hidden defects after buying a property? The legal situation in Austria regarding warranty periods, claims for recourse, etc.</a></p>
              
              <h6 class="mb-4">What is forbidden at the transfer fee?</h6>
              
              <p><a href="#" class="a_color text-decoration-underline">In Austria,</a> the so-called "black transfer" is forbidden for real estate: If a landlord or manager demands a certain amount so that the person looking for the rental contract receives it at all, this is against the law. This includes statements such as "5,000 euros or you won't get the lease" (source: www.mietervereinigung.at). Even if it means that you won't get the <a href="#" class="a_color text-decoration-underline">apartment</a> you want , you should definitely not respond to such demands - you will not have much joy with an unlawful landlord during the entire rental contract period.</p>
              
              <h6 class="mb-4">What can a fee be requested for?</h6>
              
              <p>In general, it is permitted to demand a fee for investments in a property that have been made in the last 20 years, improve the apartment and can be used beyond the lease term.</p>
              <p>This does not include, for example, painting an apartment. In general, basic equipment items cannot be replaced - and basic equipment includes heating, bathroom, toilet, kitchen stove, sink, floor and wall coverings, baseboards, fittings, wall paint, etc. (source: www.mietervereinigung.at).</p>
              <p>But beware: You can very well charge a fee for investments in long-term improvements and these may well involve a new bathroom with a better hot water boiler and a toilet (which was previously in the corridor). The point here is that no compensation can be demanded for the mere existence of the basic equipment, but very much so if investments have been made in decisive structural improvements under the conditions described above (in the last 20 years, etc.).</p>
              <p>For further details, please contact legal experts or the tenants' association.</p>
              <p>A transfer is also permitted for furniture, since legally it is actually about the sale / purchase of this furniture. However, a new tenant is not obliged to take over the furniture and interior design.</p>
              
              <h6 class="mb-4">What amount or amount is permitted as a transfer?</h6>
              
              <p>You can't do that with a specific amount of money.</p>
              <p>However, various depreciation rates apply to the individual investments: 10% loss in value per year for floors, heating, bathrooms, toilets or electrics. 5% depreciation per year for other investments (as long as they are not publicly funded, then the duration of the funding is decisive).</p>
