              <p>Depending on the type of rental property and the type and duration of use, a rental relationship can be based on one of the following legal bases:</p>
              <h6 class="mb-4">1) Full scope of application of the Tenancy Law</h6>
              <p>In principle, rents for apartments and business premises of all kinds, including the associated house and floor space (e.g. parking lot), are subject to the Tenancy Act in its full (tenant-friendly) scope ("full scope of application").</p>
              <p>Exceptions are set out in the following points.</p>
              <h6 class="mb-4">2) Partial scope of the Tenancy Law Act</h6>
              
              <p>For certain objects, the MRG only applies to a very limited extent. This mainly affects...</p>
              <p>(a) Leased objects in the creation of which the owner has invested significantly, as well as leased objects in buildings that are used without public funding after 30.6. 1953 issued building permit were built,</p>
              <p>(b) Rented objects in <a href="#" class="a_color text-decoration-underline">condominiums</a> , whose building permit according to 8.5. was granted in 1945,</p>
              <p>(c) certain attic conversions.</p>
              <p>In this so-called "sub-application area", only the MRG provisions on protection against dismissal and tenancy law in the event of the tenant's death (so-called "relatives' right of entry" - read also about taking over a rental agreement: conditions of the right of entry), apply, but not the rent restrictions of the MRG .</p>
              
              <h6 class="mb-4">3) Non-application of the Tenancy Act</h6>
              
              <p>Various tenancies are not subject to the MRG (“area of non-application”): This includes in particular buildings with a maximum of two independent apartments, service apartments, holiday apartments or <a href="tenancies.php" class="a_color text-decoration-underline">tenancies with a 6-month time limit</a> , but also leasing in the context of student or retirement homes or pure space rentals.</p>
              <p>However, if pure areas (e.g. a garage space) - which per se would not be subject to the MRG - are rented with an object subject to the MRG (e.g. with an apartment), the entire rental object - and thus also the "pure area" - is subject to the provisions of the MRG MRG.</p>
              <p><span class="text-decoration-underline">However, if the MRG is actually not applicable, only the provisions of the General Civil Code (ABGB) apply. In these cases, the tenant</span> does not enjoy any protection against dismissal, i.e. the landlord is not bound to specific reasons in the event of termination, but only to specific <a href="#" class="a_color text-decoration-underline">termination dates</a> . In addition, the formation of the <a href="#" class="a_color text-decoration-underline">rent</a> in the non-application area is not subject to any restrictions (there are exceptions, such as "usury").</p>
              
              <p>Whether the MRG is applicable to an apartment or not is a question of the individual case and depends on the apartment and use. The tenancies in a <a href="#" class="a_color text-decoration-underline">house in Austria</a>may also be subject to different legal bases. However, if the MRG is applicable (in whole or in part), it cannot be excluded by agreement of the parties at the expense of the tenant. The provisions of the MRG are mandatory law.</p>
              
