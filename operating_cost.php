              <p>Operating costs represent a not insignificant part of the <a href="#" class="text-decoration-underline a_color">rent</a> . It is therefore worth knowing a little more about them.</p>
              <div class="col-12 ps-5"><img src="./assets/img/Betriebskosten-Immobilien-Oesterreich-Hoehe-Rechnung-768x512.jpg" alt="" class="w-75"></div>
              <h6 class="mb-4 mt-4">Definition of operating costs</h6>
              
              <P>Operating costs are costs incurred by the owners of the operation of a building. The operating costs are broken down in relation to the usable area of the respective rental property and the total area of the <a href="#" class="a_color text-decoration-underline">house</a> .</P>
              <p>In the case of tenancies subject to the <a href="#" class="a_color text-decoration-underline">Tenancy Law (MRG),</a> the law regulates exactly what can be charged to tenants as operating costs. According to the MRG, the costs for</p>
              <ul style="color: var(--text-color);">
                <li>Water supply</li>
                <li>Chimneysweeps</li>
                <li>Sewer clearing</li>
                <li>Garbage and refuse removal</li>
                <li>Pest control</li>
                <li>Lighting of common parts of the home</li>
                <li>Appropriate home insurance</li>
                <li>Administrative costs</li>
                <li>Caretaker or appropriate home care</li>
                <li>Public dues</li>
              </ul>
              <p>In addition, the costs of operating communal facilities (e.g. elevator, green area, laundry room, common rooms such as saunas, etc.) can also be charged to the <a href="#" class="a_color text-decoration-underline">tenants</a> . Repair costs are by no means operating costs.</p>
              <p>In the case of <a href="#" class="a_color text-decoration-underline">apartments</a> that are subject to the General Civil Code (ABGB), the free contractual agreement has priority. If there is no such clause, Section 1099 ABGB applies. Then all costs are to be borne by the landlord.</p>
              <p><a href="terms.php" class="a_color text-decoration-underline">Legal dictionary: Overview of all legal terms of housing law and real estate law</a></p>
              
              <h6 class="mb-4 mt-4">How does the utility bill work?</h6>
              
              <p>In practice, the so-called "annual flat-rate settlement" is used almost exclusively for the settlement of operating costs. With this form of accounting, monthly flat rates are collected from the tenants and the operating costs of the entire calendar year for the whole house up to 30.6. of the following year (operating cost statement).</p>
              <p>All operating costs (expenditures) must be listed in this statement and the income, essentially the lump sums paid monthly by the tenants, must be compared with the expenses.</p>
              <p>If the expenses are more than the income, the settlement results in a balance in favor of the landlord and vice versa in favor of the tenants. The tenants have to settle the balance in favor of the landlord proportionately, conversely the landlord has to pay out the balance in favor of the tenants (= credit for the tenants) to the tenants proportionately.</p>
              
              <h6 class="mb-4 mt-4">Insight & review of costs</h6>
              
              <p>The utility bill to be drawn up by the landlord must be made available to the tenants at a suitable place (e.g. caretaker or a tenant of the house) for inspection, and the tenants must also be able to inspect the documents relating to the bill.</p>
              <p>If you have doubts about the correctness of the operating costs charged, you can request a review of the operating costs at the arbitration board or, outside of Vienna, at the responsible district court. Operating cost accounts can only be disputed within a period of 3 years (exception: cooperative housing).</p>
              <p>It is best to find out about the amount of operating costs when looking for a <a href="#" class="a_color text-decoration-underline">property</a> . Write down the operating costs and all other details of the property in a checklist - so you don't forget anything during and after the viewing.</p>
              <p>>> Here you can <a href="#" class="a_color text-decoration-underline">download a printable checklist for property viewings</a> .</p>
              
              <h6 class="mb-4 mt-4">Misconception about the term "operating costs"</h6>
              
              <p>A common misconception is that gas and electricity, insurance, telephone and internet are also part of the operating costs. They don't. Rather, these are the running costs that a tenant (can) incur in addition.</p>
